piano_keys = ["a", "b", "c", "d", "e", "f", "g"]
#           0    1    2    3    4    5    6    7
print(piano_keys[2:5])
print(piano_keys[3:7])
print(piano_keys[2:])     # anything after
print(piano_keys[:6])     # anything before
print(piano_keys[1:6:2])  # last number is step = 2
print(piano_keys[::2])    # get every 2nd item of everything in list
print(piano_keys[::-1])   # reverse order of everything in list
print(piano_keys[::-2])   # reverse order of everything in list getting 2nd item
